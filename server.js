// URL FETCHER — fetch and extract text from a URL
// ============================================================================
async function fetchURL(url) {
  return new Promise((resolve) => {
    try {
      const parsed = new URL(url.startsWith('http') ? url : `https://${url}`);
      const client = parsed.protocol === 'https:' ? https : http;

      const req = client.get(parsed.toString(), {
        timeout: 10000,
        headers: {
          'User-Agent': 'Mozilla/5.0 (compatible; PitcherSalesBot/1.0)',
          'Accept': 'text/html,application/xhtml+xml,*/*'
        }
      }, (res) => {
        if (res.statusCode >= 300 && res.statusCode < 400 && res.headers.location) {
          // Follow redirect (simple, one level)
          fetchURL(res.headers.location).then(resolve);
          return;
        }

        let data = '';
        res.on('data', chunk => { data += chunk; });
        res.on('end', () => {
          // Strip HTML tags and clean up
          const text = data
            .replace(/<script[^>]*>[\s\S]*?<\/script>/gi, ' ')
            .replace(/<style[^>]*>[\s\S]*?<\/style>/gi, ' ')
            .replace(/<[^>]+>/g, ' ')
            .replace(/\s{3,}/g, '\n\n')
            .replace(/&amp;/g, '&')
            .replace(/&lt;/g, '<')
            .replace(/&gt;/g, '>')
            .replace(/&quot;/g, '"')
            .replace(/&#39;/g, "'")
            .replace(/&nbsp;/g, ' ')
            .trim()
            .substring(0, 12000);
          resolve({ success: true, content: text });
        });
      });

      req.on('error', (err) => resolve({ success: false, error: err.message }));
      req.on('timeout', () => {
        req.destroy();
        resolve({ success: false, error: 'Request timed out' });
      });

    } catch (err) {
      resolve({ success: false, error: err.message });
    }
  });
}

