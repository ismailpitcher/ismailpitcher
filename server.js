// Main analysis endpoint — streams via SSE
app.post('/api/analyze', async (req, res) => {
  const { skill, target, apiKey, fetchSite } = req.body;

  const key = apiKey || process.env.ANTHROPIC_API_KEY;
  if (!key) {
    return res.status(400).json({ error: 'Anthropic API key required. Add it in Settings or set ANTHROPIC_API_KEY env var.' });
  }

  const prompt = SKILL_PROMPTS[skill];
  if (!prompt) {
    return res.status(400).json({ error: `Unknown skill: ${skill}` });
  }

  if (!target || !target.trim()) {
    return res.status(400).json({ error: 'Target (company name or URL) is required.' });
  }

  // SSE headers
  res.setHeader('Content-Type', 'text/event-stream');
  res.setHeader('Cache-Control', 'no-cache');
  res.setHeader('Connection', 'keep-alive');
  res.setHeader('X-Accel-Buffering', 'no');

  const send = (data) => {
    res.write(`data: ${JSON.stringify(data)}\n\n`);
  };

  // Fetch website content if target looks like a URL
  let siteContent = null;
  const isURL = target.match(/^(https?:\/\/|www\.)[^\s]+/) || target.match(/^[a-z0-9-]+\.[a-z]{2,}/i);

  if (isURL && fetchSite !== false) {
    send({ type: 'status', message: `Fetching ${target}...` });
    const result = await fetchURL(target);
    if (result.success) {
      siteContent = result.content;
      send({ type: 'status', message: `Website content loaded (${Math.round(siteContent.length / 1000)}k chars). Analyzing...` });
    } else {
      send({ type: 'status', message: `Could not fetch website (${result.error}). Analyzing from knowledge base...` });
    }
  }

  const client = new Anthropic({ apiKey: key });
  const model = req.body.model || process.env.CLAUDE_MODEL || 'claude-opus-4-5-20251001';

  try {
    const stream = client.messages.stream({
      model,
      max_tokens: 8096,
      system: prompt.system,
      messages: [{
        role: 'user',
        content: prompt.user(target, siteContent)
      }]
    });

    stream.on('text', (text) => {
      send({ type: 'text', text });
    });

    stream.on('error', (err) => {
      send({ type: 'error', message: err.message });
    });

    await stream.finalMessage();
    send({ type: 'done' });

  } catch (err) {
    send({ type: 'error', message: err.message || 'Analysis failed. Check your API key.' });
  }

  res.end();
});

